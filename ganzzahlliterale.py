## B.4.1.4 Ganzzahl- und Gleitpunktliterale



dezimal = 15
binaer = bin(dezimal) # Umwandlung von Dezimal auf Binär
hexadezimal = hex(dezimal) # Umwandlung von Dezimal auf Hexadezimal
oktal = oct(dezimal) # Umwandlung von Dezimal auf Oktal

print(f"Dezimale Darstellung = {dezimal}")
print(f"Binäre Darstellung = {binaer}")
print(f"Hexadezimale Darstellung = {hexadezimal}")
print(f"Oktale Darstellung {oktal}")